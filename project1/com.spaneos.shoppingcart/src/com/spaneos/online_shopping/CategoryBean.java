package com.spaneos.online_shopping;


public class CategoryBean {

	private String cat, des;

	public CategoryBean() {
		super();
	}

	public String getCat() {
		return cat;
	}

	public void setCat(String cat) {
		this.cat = cat;
	}

	public String getDes() {
		return des;
	}

	public void setDes(String des) {
		this.des = des;
	}

}